var productos = [];
var cantidad = [];
$(document).ready(function () {
    GetProducts()

});


function GetProducts() {
    $.ajax({
        type: "GET",
        url: BaseUrl + "/Dashboard/GetProducts",
    }).done(function (data, index) {

        data.data.forEach(function (item) {
            productos.push( item.producto );
            cantidad.push(item.cantidad);
        });
        BarChart();

    })

}
function BarChart() {
    var ctxB = document.getElementById("barChart").getContext('2d');
    var myBarChart = new Chart(ctxB, {
        type: 'bar',
        data: {
            labels: productos,
            datasets: [{
                label: 'Cantidad vendida',
                data: cantidad,
                backgroundColor: [
                    'rgba(255, 99, 132, 0.2)',
                    'rgba(54, 162, 235, 0.2)',
                    'rgba(255, 206, 86, 0.2)',
                    'rgba(75, 192, 192, 0.2)',
                    'rgba(153, 102, 255, 0.2)',
                    'rgba(255, 159, 64, 0.2)'
                ],
                borderColor: [
                    'rgba(255,99,132,1)',
                    'rgba(54, 162, 235, 1)',
                    'rgba(255, 206, 86, 1)',
                    'rgba(75, 192, 192, 1)',
                    'rgba(153, 102, 255, 1)',
                    'rgba(255, 159, 64, 1)'
                ],
                borderWidth: 1
            }]
        },
        options: {
            scales: {
                yAxes: [{
                    ticks: {
                        beginAtZero: true
                    }
                }]
            }
        }
    });
}
